void pencet(int &x, int button, int interval) {
  int y = button * 2;
  w[y] = millis() % 300000;

  if (digitalRead(tombol[button]) == 0 && w[y] - w[y + 1] > interval) {
    x++;
    k[button] = 0;
    w[y + 1] = w[y];
  } else k[button] = 1;

}
void pencet(int &x, int button, int z, int interval) {
  int y = button * 2;
  w[y] = millis() % 300000;

  if (digitalRead(tombol[button]) == 0 && w[y] - w[y + 1] > interval) {
    if (z)x++;
    else x--;
    k[button] = 0;
    w[y + 1] = w[y];
  } else k[button] = 1;

}
