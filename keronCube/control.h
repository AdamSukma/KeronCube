void joyy(int &x, int interval) {

  w[8] = millis() % 300000;
  if (analogRead(A4) < 400 && w[8] - w[9] > interval) {
    x++;
    w[9] = w[8];
  }
  else if (analogRead(A4) > 700 && w[8] - w[9] > interval) {
    x--;
    w[9] = w[8];
  }


}

